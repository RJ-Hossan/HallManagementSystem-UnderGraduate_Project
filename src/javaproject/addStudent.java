    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            if(verifyFields()){
                String query = "INSERT INTO studentdetails values(?,?,?,?,?,?)";

                pst = con.prepareStatement(query);

                pst.setString(1, jTextField1.getText());
                pst.setString(2, jTextField2.getText());
                pst.setString(3, jTextField3.getText());
                pst.setString(4, jTextField4.getText());
                pst.setString(5, jTextField5.getText());
                pst.setString(6, jTextField6.getText());

                pst.execute();
                JOptionPane.showMessageDialog(this, "Corresponding student details inserted into the corresponding room.");
                
                clear();
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    
    public boolean verifyFields(){
        String field1 = jTextField1.getText();
        String field2 = jTextField2.getText();
        String field3 = jTextField3.getText();
        String field4 = jTextField4.getText();
        String field5 = jTextField5.getText();
        String field6 = jTextField6.getText();
        
        if(field1.trim().equals("") || field2.trim().equals("") || field3.trim().equals("") || field4.trim().equals("") || field5.trim().equals("") || field6.trim().equals("")){
            JOptionPane.showMessageDialog(null, "One or more fields remain empty.", "Error - 101 : Empty Fields", 2);
            return false;
        }
        return true;
    }
    
    
