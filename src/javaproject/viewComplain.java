    public ArrayList<User1> userList(){
        ArrayList<User1> userList = new ArrayList<>();
        
        try {
            con = connection.connect();
            
            String query = "SELECT * FROM complain";
            pst = con.prepareStatement(query);
            
            ResultSet rs = pst.executeQuery();
            // DefaultTableModel tm = (DefaultTableModel)jTable1.getModel();
            // tm.setRowCount(0);
            User1 user;
            while(rs.next()){
                user = new User1(rs.getInt("ID"), rs.getString("content"), rs.getString("date"));
                userList.add(user);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return userList;
    }
    
    public void show_user(){
        ArrayList<User1> list = userList();
        DefaultTableModel tm = (DefaultTableModel)jTable1.getModel();
        Object[] row = new Object[3];
        for(int i=0; i<list.size();i++){
            row[0] = list.get(i).getID();
            row[1] = list.get(i).getContent();
            row[2] = list.get(i).getDate();
            tm.addRow(row);
        }   
    }
    
    
    
