    public boolean verifyFields(){
        String field1 = jTextField1.getText();
        String field2 = jTextField2.getText();
        String field3 = jTextField3.getText();
        String u_type = jComboBox1.getSelectedItem().toString();
        
        if(field1.trim().equals("") || field2.trim().equals("") || field3.trim().equals("") || u_type.trim().equals("")){
            JOptionPane.showMessageDialog(null, "One or more fields remain empty.", "Error - 101 : Empty Fields", 2);
            return false;
        }
        return true;                    
    }//GEN-LAST:event_jButton1ActionPerformed

    
